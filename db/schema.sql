-- schema.sql — 시연용 Sample DB 스키마 (금융 도메인: 고객 마스터 + 거래 명세)
--
-- ⚠️ 이 스키마는 *시연용 sample DB* 다. 실 운영에서는 마이그레이션 팀이 설계한
--    진짜 비즈니스 스키마(고객 마스터·거래 명세 등 — 도메인 깊이가 다름)가 사용된다.
--    인수인계 시 정직원이 실 클라이언트 스키마로 교체해야 한다.
--
-- 인코딩 원칙 (D-018):
--   - PostgreSQL DB 내부는 표준 UTF-8. (DB 인코딩을 Shift-JIS로 설정하지 않는다.)
--   - As-Is / To-Be 출력 CSV 파일만 Shift-JIS.
--   - 파일 ↔ DB 경계에서만 Python 레벨(encoding='shift_jis')로 디코드/인코드.
--   - 비교는 파일 레벨에서 일어나므로 DB 내부 인코딩과 무관하게 성립한다.
--   - 따라서 이 파일(.sql)도 UTF-8로 저장되며, 한자·가나가 그대로 들어간다.
--
-- 제약 정책 (프로토 범위): PK · NOT NULL 만 사용.
--   FK · 인덱스 · 트리거 · 뷰 · 시퀀스는 의도적으로 두지 않는다 (프로토 범위 초과).
--
-- 적용 방법: docs/SETUP.md 참조.
--   psql -h localhost -U postgres -d compare_proto -f db/schema.sql
-- =============================================================================

-- 재적용 가능하도록 기존 테이블을 먼저 제거한다 (시연 환경 초기화용).
DROP TABLE IF EXISTS transaction_log;
DROP TABLE IF EXISTS customer_master;

-- -----------------------------------------------------------------------------
-- 1) customer_master — 고객(계좌) 마스터
-- -----------------------------------------------------------------------------
CREATE TABLE customer_master (
    customer_id  TEXT    NOT NULL,          -- 고객번호 (예: C0001)
    name         TEXT    NOT NULL,          -- 한자 성명 (예: 田中太郎)
    kana         TEXT    NOT NULL,          -- 후리가나 (예: タナカタロウ)
    birth_date   DATE    NOT NULL,          -- 생년월일
    branch_code  TEXT    NOT NULL,          -- 지점코드 (예: 101)
    account_type TEXT    NOT NULL,          -- 계좌종류 (普通 / 当座)
    balance      BIGINT  NOT NULL,          -- 잔액 (엔)
    opened_date  DATE    NOT NULL,          -- 계좌 개설일
    PRIMARY KEY (customer_id)
);

-- -----------------------------------------------------------------------------
-- 2) transaction_log — 거래 명세
--    customer_id 는 customer_master 와 논리적으로 연결되지만,
--    프로토 범위상 FK 제약은 두지 않는다 (단순 컬럼).
-- -----------------------------------------------------------------------------
CREATE TABLE transaction_log (
    tx_id         TEXT    NOT NULL,         -- 거래번호 (예: T00001)
    customer_id   TEXT    NOT NULL,         -- 고객번호 (FK 아님)
    tx_date       DATE    NOT NULL,         -- 거래일
    tx_type       TEXT    NOT NULL,         -- 거래구분 (入金 / 出金 / 振込)
    amount        BIGINT  NOT NULL,         -- 거래금액 (엔)
    balance_after BIGINT  NOT NULL,         -- 거래후잔액 (엔)
    branch_code   TEXT    NOT NULL,         -- 취급지점
    memo          TEXT,                     -- 적요 (비어있을 수 있음)
    PRIMARY KEY (tx_id)
);

