def _csv_bytes(rows: list[list[str]]) -> bytes:
    """헤더 + 행을 CSV(\\n) 텍스트로 만들어 Shift-JIS 바이트로 인코딩한다."""
    buf = io.StringIO(newline="")
    writer = csv.writer(buf, lineterminator="\n")
    writer.writerow(HEADER)
    writer.writerows(rows)
    return buf.getvalue().encode(_ENCODING)


def main() -> None:
    _INPUT_DIR.mkdir(parents=True, exist_ok=True)
    for shell_id, rows in sorted(SAMPLES.items()):
        path = _INPUT_DIR / f"{shell_id}.csv"
        path.write_bytes(_csv_bytes(rows))
        print(f"[samples] {path.relative_to(_ROOT)} ({len(rows)}행)")


if __name__ == "__main__":
    main()
